using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

// ----------------------------------------------------------------
public class PowerupPoint
{
  public int X = 0;
  public int Y = 0;
  public int PowerupIndex = 0;

  // ----------------------------------------------------------------
  public PowerupPoint(int x, int y, int powerupIndex)
  {
    X = x;
    Y = y;
    PowerupIndex = powerupIndex;
  }
}
